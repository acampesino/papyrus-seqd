package org.eclipse.papyrus.uml.diagram.sequence.diagram.model.internal.layout;

public class MPoint {

	private int x, y;

	public MPoint(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	protected void setY(Integer y) {
		this.y = y;
	}

	@Override
	public String toString() {
		return "MPoint [x=" + x + ", y=" + y + "]";
	}

}
